function translateDetail(detail: string, vehicle: VehicleInput, language: TranslatedTuneLanguage) {
  if (detail.startsWith('Zielgeschwindigkeit ')) {
    const target = detail.replace('Zielgeschwindigkeit ', '');
    if (language === 'fr') return `Vitesse cible ${target}`;
    if (language === 'it') return `Velocità target ${target}`;
    return `Target speed ${target}`;
  }

  const gearMatch = detail.match(/^(\d+)\s/);
  if (gearMatch) {
    if (language === 'fr') return `${vehicle.gearCount} vitesses`;
    if (language === 'it') return `${vehicle.gearCount} marce`;
    return `${vehicle.gearCount} gears`;
  }

  return localizedDetails[language][detail] ?? englishDetails[detail] ?? detail;
}

function ordinalGearLabel(value: string, language: TranslatedTuneLanguage) {
  const match = value.match(/^(\d+)\. Gang$/);
  if (!match) {
    return null;
  }

  const gear = Number(match[1]);
  if (language === 'fr') {
    return `${gear === 1 ? '1re' : `${gear}e`} vitesse`;
  }

  if (language === 'it') {
    return `${gear}a marcia`;
  }

  const suffix = gear === 1 ? 'st' : gear === 2 ? 'nd' : gear === 3 ? 'rd' : 'th';
  return `${gear}${suffix} Gear`;
}

const localizedStockValues: Record<TranslatedTuneLanguage, string> = {
  en: 'Stock',
  fr: 'Série',
  it: 'Serie',
};

function translateValue(section: TuneSection, value: TuneValue, vehicle: VehicleInput, language: TranslatedTuneLanguage): TuneValue {
  const valueLabels = localizedValueLabels[language];

  return {
    ...value,
    label:
      section.id === 'gearing'
        ? (ordinalGearLabel(value.label, language) ?? valueLabels[section.id]?.[value.label] ?? value.label)
        : (valueLabels[section.id]?.[value.label] ?? value.label),
    detail: translateDetail(value.detail, vehicle, language),
    instruction: value.instruction
      ? (localizedInstructions[language][value.instruction] ?? englishInstructions[value.instruction] ?? value.instruction)
      : undefined,
    formula: value.formula ? (localizedFormulas[language][value.formula] ?? englishFormulas[value.formula] ?? value.formula) : undefined,
    value: value.value === 'Serie' ? localizedStockValues[language] : value.value,
  };
}

