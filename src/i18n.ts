export function localizeTuneResult(result: TuneResult, vehicle: VehicleInput, intent: TuningIntent, language: AppLanguage): TuneResult {
  if (language === 'de') {
    return result;
  }

  const tuneLanguage: TranslatedTuneLanguage = language === 'fr' || language === 'it' ? language : 'en';

  return {
    ...result,
    summary: `${vehicle.carClass} ${vehicle.drivetrain} ${localizedSurfaceNames[tuneLanguage][vehicle.surface]} Setup`,
    notes: localizedNotes(vehicle, intent, tuneLanguage),
    sections: result.sections.map((section) => ({
      ...section,
      title: localizedSectionTitles[tuneLanguage][section.id] ?? englishSectionTitles[section.id] ?? section.title,
      values: section.values.map((value) => translateValue(section, value, vehicle, tuneLanguage)),
    })),
  };
}
